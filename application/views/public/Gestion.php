                        <div class="collapsible-header">Resultado Nº 6</div>
                        <div class="collapsible-body">
                            <p><b> Código POA: 1.11.1 </b> </p>

                            <p> <b> Nombre:</b> Elaboración de los Lineamientos de implementación de la Estrategia de Desarrollo
                                Inclusivo Basada en la Comunidad.</p>
                            <p><b>Beneficiarios:</b> Personas con discapacidad, cónyuges, padres, madres y tutores de Personas con
                                Discapacidad.</p>
                            <p><b>Alcance:</b> Nacional.</p>
                            <p><b>Lugar de realización: </b> Ciudad de La Paz.</p>
                            <p><b> Fecha de inicio fecha de conclusión:</b> 02 de julio de 2018 al 28 de diciembre de 2018.</p>
                            <p><b>Resumen ejecutivo</b></p>
                            <p>
                                En cumplimiento de la Ley General de la Persona con Discapacidad N° 223, en su Artículo 46, inciso a)
                                establece que se debe tomar acciones para generar la equiparación de oportunidades de las Personas con
                                Discapacidad.
                            </p>
                            <p>
                                Por tal motivo, los lineamientos para la implementación de la Estrategia de Desarrollo Inclusivo Basado
                                en la Comunidad será el punto de partida tanto para los proyectos en curso, como para aquellos que se
                                vayan a iniciar, que han sido impulsados por organizaciones locales, líderes comunitarios, organismos no
                                gubernamentales externos al territorio, sector público y privado, entre otros.
                            </p>

                            <p>Estos lineamientos permitirán a los diferentes actores que trabajan la Estrategia de Desarrollo
                                Inclusivo Basado en la Comunidad tener la claridad, precisión y orientación frente a qué es, cómo
                                implementarla, qué actores involucrar, entre otros aspectos, facilitando una mejor gestión de los
                                programas que se desarrollen, en pro de la inclusión de la Personas con Discapacidad, sus familias y/o
                                cuidadores y comunidades, a partir del reconocimiento de la diversidad y multiculturalidad de nuestro
                                país.</p>
                            <p>Es importante resaltar, que estos lineamientos están siendo construido a partir de las experiencias
                                rescatadas de nuestro país y estarán en continua actualización, debido a que las dinámicas de los
                                procesos sociales son cambiantes y que el trabajo permanente que realizan las organizaciones sociales y
                                los diferentes entes gubernamentales nacionales y territoriales generará insumos relevantes para todas
                                las organizaciones que implementan la estrategia a nivel nacional.</p>
