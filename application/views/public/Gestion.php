                            <p><b> Código POA: 1.3.1 </b> </p>

                            <p> <b> Nombre:</b> Elaboración del manual de Buen Trato a las Personas con Discapacidad en los Servicios
                                de Justicia.</p>
                            <p><b>Beneficiarios:</b>Personas con y sin discapacidad, cónyuges, padres y madres de Personas con
                                Discapacidad, así como las instituciones que trabajan en materia de discapacidad.</p>
                            <p><b>Alcance:</b> Nacional.</p>
                            <p><b>Lugar de realización: </b> Ciudad de La Paz.</p>
                            <p><b> Fecha de inicio fecha de conclusión:</b> Del 1 de Agosto de 2018 al 31 de diciembre de 2018.</p>
                            <p><b>Resumen ejecutivo</b></p>
                            <p> En cumplimiento a la Constitución Política del Estado que establece en su Artículo 71, Parágrafo II
                                que el Estado adaptara las medidas de acción positiva para promover la efectiva integración de las
                                Personas con Discapacidad en diferentes ámbitos. </p>
                            <p> La Ley General de la Persona con Discapacidad, que en Artículo 46, inciso d) establece que se
                                promoverá
                                y fomentara el enfoque de discapacidad bajo un modelo social de derechos en el marco de una cultura de
                                la dignidad.</p>
                            <p> Por lo mencionado, se asume el compromiso de generar condiciones de Buen Trato en las relaciones inter
                                e
                                intrapersonales, así como en las instituciones de servicio público, a fin de establecer ambientes de
                                comunicación y diálogo saludable.</p>
                            <p> En respuesta a esta necesidad y las demandas de las Organizaciones de las Personas con Discapacidad
                                según el acuerdo firmado en las mesas técnicas de trabajo de 2016 (punto 2) entre el Gobierno Central y
                                las Organizaciones de Personas con Discapacidad, se presenta el documento “Bases Comunicacionales y
                                Actitudinales para el Buen Trato de las Personas con y sin Discapacidad en los Servicios de Justicia”.
                            </p>





