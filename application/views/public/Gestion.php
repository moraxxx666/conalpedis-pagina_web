                        <div class="collapsible-header">Resultado Nº 2.2</div>
                        <div class="collapsible-body">
                            <p><b> Código POA: 1.3.1 </b> </p>

                            <p> <b> Nombre:</b> Presentación de los Discuentos, escrito desde la realidad de las Personas con
                                Discapacidad.</p>
                            <p><b>Beneficiarios:</b> Personas con discapacidad, cónyuges, padres y madres de Personas con
                                Discapacidad.</p>
                            <p><b>Alcance:</b> Nacional.</p>
                            <p><b>Lugar de realización: </b> Ciudad de La Paz.</p>
                            <p><b> Fecha de inicio fecha de conclusión:</b> Abril de 2018</p>
                            <p><b>Resumen ejecutivo</b></p>
                            <p>
                                La Ley General de la Persona con Discapacidad, que en Artículo 46, inciso d) establece que se promoverá
                                y fomentara el enfoque de discapacidad bajo un modelo social de derechos en el marco de una cultura de
                                la dignidad.
                            </p>
                            <p>Por lo mencionado y con el objetivo de promover y difundir la inclusión social y educativa de las
                                personas con discapacidad, el Comité Nacional de la Persona con Discapacidad (CONALPEDIS) emprende una
                                grata y enriquecedora experiencia en favor de las personas con discapacidad.</p>
                            <p> Lanzando la segunda versión de los Discuentos con la esperanza de que niños, niñas y jóvenes con
                                discapacidad entre los 12 y 17 años de edad de diferentes Unidades Educativas puedan darnos a conocer
                                sus experiencias acerca de la inclusión educativa y social de las personas con discapacidad.</p>
                            <p>De esta nueva experiencia, el 13 de noviembre de 2017 aproximadamente 45 niños, niñas y jóvenes con
                                discapacidad participaron entregando sus cuentos, de los cuales dos han sido los ganadores y de quienes
                                han sido publicados sus cuentos, siendo los sus autores Verónica Herrera Alcón con el cuento “Amor sin
                                límites” y Nayra Huanca Mamani con el cuento “Reflejando mi alma, mediante una carta”.</p>

                            <p> La presentación y entrega de reconocimiento se realizó en el Salón de la Honorable Cámara de Diputados
                                con presencia de diferentes autoridades tanto del Poder Ejecutivo como Legislativo entre las que se
                                puede mencionar a la Honorable Ana María Díaz, el Dr. Diego Jimenez Viceministro de Derechos
                                Fundamentales y la Dra. Estefanía Morales Viceministra de Igualdad de Oportunidades, ambos del
                                Ministerio de Justicia y Transparencia Institucional</p>





