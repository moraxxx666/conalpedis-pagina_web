                        <div class="collapsible-header">Resultado Nº 5.1</div>
                        <div class="collapsible-body">
                            <p><b> Código POA: 1.10.2 </b> </p>

                            <p> <b> Nombre:</b> Entrega del proyecto de Centro Día Comunitario y Residencia para Personas con
                                Discapacidad Psíquica o Mental.</p>
                            <p><b>Beneficiarios:</b> Los beneficiarios directos son las Personas con Discapacidad Mental o Psíquica
                                que han sido diagnosticadas con trastornos psíquicos después de los diez y seis (16) años de edad.</p>
                            <p><b>Alcance:</b> Nacional.</p>
                            <p><b>Lugar de realización: </b> Ciudad de La Paz.</p>
                            <p><b> Fecha de inicio fecha de conclusión:</b> del 01 de junio de 2018 al 26 de octubre de 2018.</p>
                            <p><b>Resumen ejecutivo</b></p>
                            <p>
                                En el marco del Artículo 46, Inciso a) de la Ley General de la Persona con Discapacidad, N° 223, se
                                establece que es atribución del CONALPEDIS tomar acciones para generar la equiparación de oportunidades
                                de las Personas con Discapacidad.
                            </p>
                            <p>
                                Por lo mencionado, los servicios que se espera brindar a través del Centro Día Comunitario para Personas
                                con Discapacidad Mental o Psíquica son talleres, terapias y actividades que permitan promover la
                                inclusión social y económica de la persona con Discapacidad Mental o Psíquica a través de la:
                            </p>
                            <ul>
                                <li class="item-lista">Prevención primaria de la Discapacidad Mental o Psíquica</li>
                                <li class="item-lista">Prevención secundaria de la Discapacidad Mental o Psíquica.</li>
                                <li class="item-lista">Prevención terciaria de la Discapacidad Mental o Psíquica.</li>
                            </ul>
                            <p>Con el fin de que las Personas con y sin Discapacidad Mental o Psíquica reciban una atención oportuna,
                                con el objetivo de que su condición no empeore y reciba el tratamiento adecuado y necesario.</p>
                        </div>
