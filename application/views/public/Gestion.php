                        <div class="collapsible-header">Resultado Nº 1</div>
                        <div class="collapsible-body">
                            <p><b> Código POA: 1.1.1 </b> </p>

                            <p> <b> Nombre:</b> Implementación del Sistema de Información de Beneficios de Personas con Discapacidad
                                (SIBPDIS –
                                BOLIVIA).</p>
                            <p><b>Beneficiarios:</b> Personas con y sin discapacidad, instituciones públicas y/o privadas.</p>
                            <p><b>Alcance:</b> Nacional.</p>
                            <p><b>Lugar de realización: </b> Ciudad de La Paz.</p>
                            <p><b> Fecha de inicio fecha de conclusión:</b> del 17 de julio de 2018 al 14 de septiembre de 2018.</p>
                            <p><b>Resumen ejecutivo</b></p>
                            <p> La Ley General de Personas con Discapacidad Nº 223, en su Artículo 46, inciso k) establece que la
                                entidad debe “realizar y actualizar un registro de los programas públicos y privados de servicio social,
                                religioso, de convenio y autoayuda de Personas con Discapacidad”.</p>
                            <p>Así también, dando cumplimiento al primer punto asumido por el CONALPEDIS en la mesa técnica de dialogo
                                entre el Gobierno Central y las Organizaciones de las Personas con Discapacidad, en la cual se acordó
                                “Desarrollar e implementar un Sistema Nacional de Información de Personas con Discapacidad”.</p>
                            <p>El Comité Nacional de la Persona con Discapacidad (CONALPEDIS) desarrolló e implementó el Sistema de
                                Información de Beneficios de Personas con Discapacidad (SIBPDIS – BOLIVIA), el cual tiene como principal
                                objetivo centralizar y registrar los beneficios que el Estado Plurinacional de Bolivia otorga a las
                                Personas con Discapacidad, monitoreando de esta forma el impacto que tienen las políticas públicas en
                                materia de discapacidad.</p>
                            <p> El Sistema de Información de Beneficios de Personas con Discapacidad (SIBPDIS – BOLIVIA) se encuentra
                                disponible en el siguiente enlace: http://sistemasibpdis.conalpedis.gob.bo/
                            </p>
                        </div>
