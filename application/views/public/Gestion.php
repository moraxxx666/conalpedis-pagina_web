                            <p><b>Lugar de realización: </b> Ciudad de La Paz.</p>
                            <p><b> Fecha de inicio fecha de conclusión:</b> del 01 de junio de 2018 al 31 de diciembre de 2018.</p>
                            <p><b>Resumen ejecutivo</b></p>
                            <p>
                                En el marco del Artículo 46, Inciso a) de la Ley General de la Persona con Discapacidad, N° 223, en la
