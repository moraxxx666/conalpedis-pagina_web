                        <div class="collapsible-header">Resultado Nº 4</div>
                        <div class="collapsible-body">
                            <p><b> Código POA: 1.7.2 </b> </p>

                            <p> <b> Nombre:</b> Anteproyecto de Ley “Accesibilidad Universal”.</p>
                            <p><b>Beneficiarios:</b> Personas con Discapacidad, mujeres en estado de gestación, niños, niñas,
                                adolescentes, personas adultas mayores y otras personas que tienen dificultad de Interacción con el
                                entorno.</p>
                            <p><b>Alcance:</b> Nacional.</p>
                            <p><b>Lugar de realización: </b> Ciudad de La Paz.</p>
                            <p><b> Fecha de inicio fecha de conclusión:</b> del 1 de julio de 2018 al 30 de septiembre de 2018.</p>
                            <p><b>Resumen ejecutivo</b></p>
                            <p>
                                El Comité Nacional de la Persona con Discapacidad (CONALPEDIS), en el marco de la Ley General de la
                                Persona con Discapacidad, en su artículo 46, inciso l) establece que se elaborará proyectos de normas
                                para la adecuación de la Ley en todos los ámbitos.
                            </p>
                            <p>
                                En virtud de las Observaciones realizadas por el Comité sobre los Derechos de las Personas con
                                Discapacidad al Estado boliviano y lo establecido en la Ley General de la Persona con Discapacidad N°
                                223 respecto a la Accesibilidad, el Ante Proyecto de Ley de “Accesibilidad Universal” busca dar
                                lineamientos de Accesibilidad Universal en los espacios y servicios de uso público en edificaciones,
                                medios de transporte, turismo, información y comunicación.
                            </p>
                            <p>Considerando que la Accesibilidad Universal debe ser entendida como el conjunto de condiciones que
                                deben cumplir los espacios de uso público, edificios, medios de transporte, servicios, turismo,
                                información y comunicación para las Personas con Discapacidad y otras personas que tienen dificultades
                                de interacción con el entorno de la manera más autónoma, segura, cómoda y natural posible.</p>
                            <p>Cabe mencionar que el Estado Plurinacional en todos sus niveles tienen la obligación de velar por la
                                inclusión de las Personas con Discapacidad en su jurisdicción territorial toda vez que la Ley General de
                                la Persona con Discapacidad N° 223, establece en su Artículo 48 las atribuciones que tienen los
                                Gobiernos Autónomos Municipales y Departamentales en la aplicación de aspectos inherentes a
                                accesibilidad y las adecuaciones que se deben realizar de manera progresiva con base en los ajustes
                                razonables.</p>
